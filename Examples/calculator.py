num1 = 20
num2 = 5

def addition():
    add = num1 + num2
    print("Addition:", add)

def subtraction():
    sub = num1 - num2
    print("Subtraction:", sub)

def multiplication():
    mul = num1 * num2
    print("Multiplication:", mul)

def division():
    div = num1 / num2
    print("Division:", div)

addition()
subtraction()
multiplication()
division()